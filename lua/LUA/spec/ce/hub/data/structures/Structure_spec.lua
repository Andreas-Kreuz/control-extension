insulate("ce.hub.data.structures.Structure", function ()
    local function clearModule(name) package.loaded[name] = nil end

    before_each(function ()
        clearModule("ce.hub.data.structures.Structure")
    end)

    it("stores values through setters and tracks dirty fields", function ()
        local Structure = require("ce.hub.data.structures.Structure")

        local structure = Structure:new("#3")
        structure:setPosition(1.11, 2.22, 3.33)
        structure:setRotation(4.44, 5.55, 6.67)
        structure:setModelType(22, "Immobilie")
        structure:setTag("Depot")
        structure:setLight(true)
        structure:setSmoke(false)
        structure:setFire(true)

        assert.same(1.11, structure:getPosX())
        assert.same(2.22, structure:getPosY())
        assert.same(3.33, structure:getPosZ())
        assert.same(4.44, structure:getRotX())
        assert.same(5.55, structure:getRotY())
        assert.same(6.67, structure:getRotZ())
        assert.same(22, structure:getModelType())
        assert.same("Immobilie", structure:getModelTypeText())
        assert.same("Depot", structure:getTag())
        assert.is_true(structure:getLight())
        assert.is_false(structure:getSmoke())
        assert.is_true(structure:getFire())
        assert.is_true(structure:hasDirtyFields())

        structure:resetDirty()
        structure:setPosition(1.11, 2.22, 3.33)
        structure:setRotation(4.44, 5.55, 6.67)
        structure:setModelType(22, "Immobilie")
        structure:setTag("Depot")
        structure:setLight(true)
        structure:setSmoke(false)
        structure:setFire(true)
        assert.is_false(structure:hasDirtyFields())
    end)
end)
